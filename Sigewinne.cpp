#include <bits/stdc++.h>
using namespace std;
#define int long long
const int maxn = 1e5 + 10;
int a[maxn], b[maxn];
int pre[maxn][2], suf[maxn][2], _suf[maxn], _pre[maxn], dp1[maxn][2][2], dp2[maxn][2][2], _dp1[maxn], _dp2[maxn];
int n;
void init()
{
    for (int i = 0; i <= n + 1; i++)
    {
        pre[i][0] = pre[i][1] = 0;
        suf[i][0] = suf[i][1] = 0;
        _pre[i] = _suf[i] = -1e18;
        for (int j = 0; j <= 1; j++)
        {
            for (int k = 0; k <= 1; k++)
            {
                dp1[i][j][k] = dp2[i][j][k] = 0;
            }
        }
        _dp1[i] = _dp2[i] = -1e18;
    }
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    int T;
    cin >> T;
    while (T--)
    {
        cin >> n;
        for (int i = 1; i <= n; i++)
            cin >> a[i];
        for (int i = 1; i <= n; i++)
            cin >> b[i];
        init();
        for (int i = 1; i <= n; i++)
        {
            pre[i][0] = pre[i - 1][0] + a[i] + b[i];
            pre[i][1] = pre[i][0] + max(a[i + 1],b[i + 1]);
        }
        for (int i = n; i >= 1; i--)
        {
            suf[i][0] = suf[i + 1][0] + a[i] + b[i];
            suf[i][1] = suf[i][0] + max(a[i - 1],b[i - 1]);
        }
        for (int i = 1; i <= n; i++)
            _suf[i] = max({_suf[i - 1],suf[i][0],suf[i + 1][1]});
        for (int i = n; i >= 1; i--)
            _pre[i] = max({_pre[i + 1],pre[i][0],pre[i - 1][1]});
        for (int i = 1; i <= n; i++)
        {
            dp1[i][0][0] = max(0ll, max(dp1[i - 1][0][1], dp1[i - 1][1][1])) + max(a[i], b[i]);
            dp1[i][0][1] = max(0ll, dp1[i][0][0]) + min(a[i], b[i]);
            dp1[i][1][0] = max(0ll, max(dp1[i - 1][0][1], dp1[i - 1][1][1])) + min(a[i], b[i]);
            dp1[i][1][1] = max(0ll, dp1[i][1][0]) + max(a[i], b[i]);
            _dp1[i] = max(_dp1[i - 1], max(dp1[i][0][0], max(dp1[i][0][1], max(dp1[i][1][0], dp1[i][1][1]))));
        }
        for (int i = n; i >= 1; i--)
        {
            dp2[i][0][1] = max(0ll, max(dp2[i + 1][1][0], dp2[i + 1][0][0])) + min(a[i], b[i]);
            dp2[i][0][0] = max(0ll, dp2[i][0][1]) + max(a[i], b[i]);
            dp2[i][1][1] = max(0ll, max(dp2[i + 1][1][0], dp2[i + 1][0][0])) + max(a[i], b[i]);
            dp2[i][1][0] = max(0ll, dp2[i][1][1]) + min(a[i], b[i]);
            _dp2[i] = max(_dp2[i + 1], max(dp2[i][0][0], max(dp2[i][0][1], max(dp2[i][1][0], dp2[i][1][1]))));
        }
        int ans = 1e18;
        for (int i = 1; i <= n; i++)
            ans = min(ans,max({
                max(0ll,_suf[i - 1] - suf[i][0]) + max(0ll,_pre[i + 1] - pre[i][0]) + a[i] + b[i],
                min(
                    max(
                        max(0ll,_suf[i - 1] - suf[i][0]) + min(a[i],b[i]),
                        max(0ll,_pre[i + 1] - pre[i][0]) + max(a[i],b[i])
                    ),
                    max(
                        max(0ll,_suf[i - 1] - suf[i][0]) + max(a[i],b[i]),
                        max(0ll,_pre[i + 1] - pre[i][0]) + min(a[i],b[i])
                    )
                ),
                max(_dp1[i - 1],_dp2[i + 1])
            }));
        cout << ans << '\n';
    }
}
